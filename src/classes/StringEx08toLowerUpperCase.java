package classes;

public class StringEx08toLowerUpperCase {

	public static void main(String[] args) {
		String sV01 = "Exciting Java ";
		String sResult = "";
		
		sResult = sV01.toLowerCase();
		System.out.println(sResult);
		
		sResult = sV01.toUpperCase();
		System.out.println(sResult);
		
	}

}
